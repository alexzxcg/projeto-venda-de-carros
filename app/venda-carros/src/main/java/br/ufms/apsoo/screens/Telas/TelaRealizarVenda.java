  private void vezesBoxActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_vezesBoxActionPerformed
    if (formaPagamentoBox.getSelectedItem().equals("Parcelado")
        || formaPagamentoBox.getSelectedItem().equals("Cartão De Crédito")) {
      String selecione = vezesBox.getSelectedItem().toString();
      if (selecione != "Selecione") {
        this.valorEntrada = Double.parseDouble(campoTextoValorEntrada.getText());
        this.numeroParcelas = Integer.parseInt(vezesBox.getSelectedItem().toString());
        double valorTemporario = (this.valorCarro - this.valorEntrada) / this.numeroParcelas;
        caixaTextoValorTotal.setText(String.format("R$ %.2f", valorTemporario));
      }
    } else {
      if (vezesBox.getSelectedItem().toString() != "Selecione") {
        this.numeroParcelas = 1;
        this.valorEntrada = 0;
      }
    }
  }// GEN-LAST:event_vezesBoxActionPerformed

  private void caixaTextoCPFClienteKeyPressed(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_caixaTextoCPFKeyPressed
    if (evt.getKeyCode() == VK_ENTER) {
      if (caixaTextoCPFCliente.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(null, "CPF não preeenchido!", null, WIDTH);
      } else {
        this.cliente = clienteController.buscar(caixaTextoCPFCliente.getText());
        if (this.cliente == null) {
          JOptionPane.showMessageDialog(null, "Cliente não cadastrado", null, WIDTH);
        } else {
          caixaTextoNomeCliente.setText(cliente.getNome());
        }
      }
    }
  }// GEN-LAST:event_caixaTextoCPFKeyPressed

  private void marcaCarro() {
    List<String> marcas = carroController.buscaMarcas();
    marcaBox.removeAllItems();
    marcas.forEach(c -> {
      marcaBox.addItem(c);
    });
  }

  private void modeloCarro() {
    List<Carro> modelos = carroController.buscaMarcaCarro(marcaSelecionada);
    modeloBox.removeAllItems();
    modelos.forEach(c -> {
      modeloBox.addItem(c.getCodigo() + " - " + c.getModelo());
    });
  }

