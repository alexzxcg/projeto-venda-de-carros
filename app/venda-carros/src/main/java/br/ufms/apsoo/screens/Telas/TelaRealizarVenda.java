  private void botaoConfirmarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_botaoConfirmarActionPerformed
    if (caixaTextoCPFCliente.getText().trim().equals("")) {
      JOptionPane.showMessageDialog(null, "CPF não preeenchido!", null, WIDTH);
    } else if (marcaBox.getSelectedItem().equals("Selecione")) {
      JOptionPane.showMessageDialog(null, "Escolha alguma marca!", null, WIDTH);
    } else if (modeloBox.getSelectedItem().equals("Selecione")) {
      JOptionPane.showMessageDialog(null, "Escolha algum modelo!", null, WIDTH);
    } else if (campoTextoValorEntrada.getText().trim().equals("")) {
      JOptionPane.showMessageDialog(null, "Digite um valor de entrada!", null, WIDTH);
    } else if (formaPagamentoBox.getSelectedItem().equals("Cheque")
        && caixaTextoCodigoGerente.getText().trim().equals("")) {
      JOptionPane.showMessageDialog(null, "Digite o código do gerente", null, WIDTH);
    }

    else {
      this.venda = vendaController.iniciarNovaVenda(this.valorEntrada, this.valorTotal, this.formaPagamento,
          this.numeroParcelas, this.funcionario.getCpf(), this.cliente.getCpf(), this.carro.getCodigo());
      if (this.venda == null) {
        JOptionPane.showMessageDialog(null, "Venda não realizada", null, WIDTH);
      } else {
        ConfirmacaoVenda telaConfirmacao = new ConfirmacaoVenda();
        telaConfirmacao.setVenda(venda);
        telaConfirmacao.setVisible(true);
      }
    }

  }// GEN-LAST:event_botaoConfirmarActionPerformed

